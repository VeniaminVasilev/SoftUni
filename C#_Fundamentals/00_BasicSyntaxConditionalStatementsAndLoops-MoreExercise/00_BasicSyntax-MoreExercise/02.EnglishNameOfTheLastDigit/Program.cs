int lastDigit = number % 10;

if (lastDigit == 1) { Console.WriteLine("one"); }
if (lastDigit == 2) { Console.WriteLine("two"); }
if (lastDigit == 3) { Console.WriteLine("three"); }
if (lastDigit == 4) { Console.WriteLine("four"); }
if (lastDigit == 5) { Console.WriteLine("five"); }
if (lastDigit == 6) { Console.WriteLine("six"); }
if (lastDigit == 7) { Console.WriteLine("seven"); }
if (lastDigit == 8) { Console.WriteLine("eight"); }
if (lastDigit == 9) { Console.WriteLine("nine"); }
if (lastDigit == 0) { Console.WriteLine("zero"); }